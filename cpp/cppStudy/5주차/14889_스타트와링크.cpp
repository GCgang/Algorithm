// https://www.acmicpc.net/problem/14889
/*
문제:
오늘은 스타트링크에 다니는 사람들이 모여서 축구를 해보려고 한다.
축구는 평일 오후에 하고 의무 참석도 아니다
축구를 하기 위해 모인 사람은 총 N명이고 신기하게도 N은 짝수이다
이제 N/2명으로 이루어진 스타트 팀과 링크 팀으로 사람들을 나눠야 한다
BOJ를 운영하는 회사 답게 사람에게 번호를 1부터 N까지로 배정했고,
아래와 같은 능력치를 조사했다.
능력치 Sij는 i번 사람과 j번 사람이 같은 팀에 속했을 때, 팀에 더해지는 능력치이다.
팀의 능력치는 팀에 속한 모든 쌍의 능력치 Sij의 합이다.
Sij는 Sji와 다를 수도 있으며, i번 사람과 j번 사람이 같은 팀에 속했을 때,
팀에 더해지는 능력치는 Sij와 Sji이다
N=4이고, S가 아래와 같은 경우를 살펴보자

i\j	1	2	3	4
1	 	1	2	3
2	4	 	5	6
3	7	1	 	2
4	3	4	5	 

예를 들어, 1, 2번이 스타트 팀, 3, 4번이 링크 팀에 속한 경우  두 팀의 능력치는 아래와 같다
스타트 팀: S12 + S21 = 1 + 4 = 5
링크 팀: S34 + S43 = 2 + 5 = 7

1, 3번이 스타트팀, 2, 4번이 링크 팀에 속하면, 두 팀의 능력치는 아래와 같다
스타트 팀: S13 + S31 = 2 + 7 = 9
링크 팀: S24 + S42 = 6 + 4 = 10

축구를 재미있게 하기 위해서 스타트 팀의 능력이와 링크팀의 능력치의 차이를 최소로 하려고 한다
위의 예제와 같은 경우에는 1, 4번이 스타트 팀, 2, 3번이 팀이 링크 팀에 속하면 스타트 팀의 능력치는 6,
링크 팀의 능력치는 6이 되어서 차이가 0이 되고 이 값이 최소이다

입력:
첫째 줄에 N(4 <= N <= 20, N은 짝수)이 주어진다.
둘째 줄부터 N개의 줄에 S가 주어진다.
각 줄은 N개의 수로 이루어져 있고, i번 줄의 j번째 수는 Sij이다.
Sij는 항상 0이고, 나머지 Sij는 1보다 크거나 같고, 100보다 작거나 같은 정수이다

출력:
첫째 줄에 스타트 팀과 링크 팀의 능력치의 차이의 최솟값을 출력한다

ex:
입력
4
0 1 2 3
4 0 5 6
7 1 0 2
3 4 5 0

출력
0
*/

#include <iostream>
#include <vector>
#include <algorithm>
using namespace std;

// 전역 변수 선언
int stat[21][21]; // 각 사람들 간의 상호 작용으로 인한 팀 능력치를 저장하는 배열
int visited[21]; // 사람이 스타트 팀에 속하는지 여부를 표시하는 배열 (방문 여부로 사용)
int n, k; // n: 참가자 총 수, k: 한 팀에 속해야 하는 사람 수 (n/2)
int ret = 987654321; // 최소 능력치 차이를 저장하는 변수, 초기값은 매우 큰 수로 설정

// 두 팀의 능력치 차이를 계산하는 함수
int go(vector<int>& a, vector<int>& b)
{
    pair<int, int> ret;
    for (int i = 0; i < n / 2; ++i)
    {
        for (int j = 0; j < n / 2; ++j)
        {
            if (i == j)
                continue; // 같은 사람끼리의 능력치는 고려하지 않음
            ret.first += stat[a[i]][a[j]]; // 스타트 팀 능력치 합산
            ret.second += stat[b[i]][b[j]]; // 링크 팀 능력치 합산
        }
    }
    return abs(ret.first - ret.second); // 두 팀의 능력치 차이의 절대값 반환
}

// 재귀적으로 팀 조합을 생성하는 함수
void combi(int start, vector<int> v)
{
    if (v.size() == k) // 기저 조건: 한 팀의 사람 수가 k와 같을 때
    {
        vector<int> start, link; // 스타트 팀과 링크 팀을 나누어 저장할 벡터
        for (int i = 0; i < n; ++i)
        {
            if (visited[i])
                start.push_back(i); // 방문한 사람은 스타트 팀에
            else
                link.push_back(i); // 방문하지 않은 사람은 링크 팀에
        }
        ret = min(ret, go(start, link)); // 두 팀의 능력치 차이 계산 후 최솟값 갱신
        return;
    }

    for (int i = start + 1; i < n; ++i)
    {
        visited[i] = 1; // i번째 사람을 방문 처리 (스타트 팀에 포함)
        v.push_back(i); // 현재 조합에 i번째 사람 추가
        combi(i, v); // 다음 사람에 대해 조합 생성 재귀 호출
        visited[i] = 0; // 재귀 호출이 끝나면 방문 처리 해제
        v.pop_back(); // 현재 조합에서 i번째 사람 제거
    }
}

int main()
{
    cin >> n; // 참가자 수 입력

    // 능력치 정보 입력
    for (int i = 0; i < n; ++i)
    {
        for (int j = 0; j < n; ++j)
        {
            cin >> stat[i][j];
        }
    }

    k = n / 2; // 한 팀에 속해야 하는 사람 수 계산

    vector<int> v; // 초기 조합 벡터 생성
    combi(-1, v); // 조합 생성 함수 호출
    cout << ret << "\n"; // 계산된 최소 능력치 차이 출력

    return 0;
}
